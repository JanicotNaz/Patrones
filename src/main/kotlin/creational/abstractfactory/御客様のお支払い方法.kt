package creational.abstractfactory

class 御客様のお支払い方法 {
    companion object {
        fun 御客様の支払い(種類の払いの変数: 種類の払い): 支払い?{
            return 種類の支払いや種類の演繹法が選ぶ().支払いを作る(種類の払いの変数)
        }
        fun 御客様の演繹法( 種類の演繹法の変数: 種類の演繹法): 演繹法?{
            return 種類の支払いや種類の演繹法が選ぶ().演繹法を作る(種類の演繹法の変数)
        }

    }
}